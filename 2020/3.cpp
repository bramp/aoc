/**
 * Prints a vector<vector<char>>.
 * 
 * @param map 
 */
void print(vector<vector<char>> map) {
    for (int y = 0; y < map.size(); y++) {
        for (int x = 0; x < map[y].size(); x++) {
            cout << map[y][x];
        }
        cout << endl;
    }
}

/**
 * Returns how many trees in the map you would hit with this slope.
 * 
 * @param map 
 * @param dx 
 * @param dy 
 * @return int 
 */
int slope(const vector<vector<char>> &map, int dx, int dy) {
    int x = dx;
    int y = dy;

    int tree = 0;
    while (y < map.size()) {
        if (map[y][x] == '#') {
            tree++;
        }

        x = (x + dx) % map[0].size();
        y += dy;
    }

    return tree;
}

int main() {
    auto ss = stringstream{input};

    vector<vector<char>> map;

    // Read the map
    std::string line; 
    while (getline(ss, line, '\n')) {
        boost::trim(line);
        if (line == "") {
            continue;
        }
        
        vector<char> row;
        for (char x : line) {
            row.push_back(x); // a tree
        }

        map.push_back(row);
    }

    // print(map);

    cout << "Answer 3.1: " << slope(map, 3, 1) << endl;

    long answer = 1;

    answer *= slope(map, 1, 1);
    answer *= slope(map, 3, 1);
    answer *= slope(map, 5, 1);
    answer *= slope(map, 7, 1);
    answer *= slope(map, 1, 2);

    cout << "Answer 3.2: " << answer << endl;
}